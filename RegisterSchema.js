const { default: mongoose } = require("mongoose");

let CustomerSchema=mongoose.Schema({
    firstname:{
        type:String,
        required:true
    },
    lastname:{
        type:String,
        required:true
    },
    password:{
        type:String,
        required:true
    },
    confirmpassword:{
        type:String,
        required:true
    },
    email:{
        type:String,
        required:true
    },
    gender:{
        type:String,
        required:true
    },
})

const Customer=mongoose.model('Customer',CustomerSchema);

module.exports=Customer;